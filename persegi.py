# Fungsi untuk menghitung luas persegi
def luas_persegi():
    sisi = float(input("Masukkan panjang sisi persegi: "))
    luas = sisi * sisi # rumus dari luas persegi panjang
    return luas

# Contoh penggunaan fungsi
print("Luas persegi adalah:", luas_persegi()) # Menampilkan Jumlah Luas Persegi Yang dihitung
